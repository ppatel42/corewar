#include "libft.h"

void	ft_putnbr(int nb)
{
	int min;

	min = 0;
	if (nb == -2147483648)
	{
		min = 1;
		nb /= 10;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	ft_putchar((nb % 10) + '0');
	if (min)
		ft_putchar('8');
}

#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	int min;

	min = 0;
	if (nb == -2147483648)
	{
		min = 1;
		nb /= 10;
	}
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb *= -1;
	}
	if (nb > 9)
		ft_putnbr_fd(nb / 10, fd);
	ft_putchar_fd((nb % 10) + '0', fd);
	if (min)
		ft_putchar_fd('8', fd);
}
